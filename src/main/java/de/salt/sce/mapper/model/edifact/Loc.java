package de.salt.sce.mapper.model.edifact;

public class Loc {

    private String locFuncCodeQualifier;
    private String locNameCode;
    private String codeListIdentCode;
    private String codeListResponsibleAgencyCode;
    private String locName;

    public String getLocFuncCodeQualifier() { return locFuncCodeQualifier; }
    public void setLocFuncCodeQualifier(String locFuncCodeQualifier) { this.locFuncCodeQualifier = locFuncCodeQualifier; }
    public String getLocNameCode() { return locNameCode; }
    public void setLocNameCode(String locNameCode) { this.locNameCode = locNameCode; }
    public String getCodeListIdentCode() { return codeListIdentCode; }
    public void setCodeListIdentCode(String codeListIdentCode) { this.codeListIdentCode = codeListIdentCode; }
    public String getCodeListResponsibleAgencyCode() { return codeListResponsibleAgencyCode; }
    public void setCodeListResponsibleAgencyCode(String codeListResponsibleAgencyCode) { this.codeListResponsibleAgencyCode = codeListResponsibleAgencyCode; }
    public String getLocName() { return locName; }
    public void setLocName(String locName) { this.locName = locName; }

}
